                <h1>Agility</h1>
                <p>Our two agility venues are <a href="https://ukagilityinternational.com/" target="_blank" rel="noopener noreferrer nofollow">UKI</a> and <a href="http://www.k9cpe.com/" target="_blank" rel="noopener noreferrer nofollow">CPE</a> (Canine Performance Events, Inc). Both venues stress the importance of having fun with your dog, and have designed their programs to do just that.</p>
                <p>All trials are held outdoors at <a href="http://webcms.pima.gov/cms/one.aspx?portalId=169&pageId=1367" target="_blank" rel="noopener noreferrer nofollow">Brandi Fenton Memorial Park</a>. Our shows are one-ring and fully fenced.</p>
                <hr>
                <p>Some of the philosophies of Desert Dawg Sports, along with its absolutes and key differences regarding agility are:</p>
                <ul class="differences">
                    <li><i class="fas fa-paw"></i>One judge shows – there will never be a ring conflict, a reason not to work a class, or see your friend’s runs.</li>
                    <li><i class="fas fa-paw"></i>We expect and appreciate people to work the show to the extent they are able; we will provide lunch, shade and a raffle ticket.</li>
                    <li><i class="fas fa-paw"></i>We solicit and encourage first-time agility dogs and handlers, including junior handlers. If you have any questions or suggestions just let us know, we’re happy to help.</li>
                    <li><i class="fas fa-paw"></i>Junior handlers enjoy discounted runs for any of our agility trials.</li>
                    <li><i class="fas fa-paw"></i>We always fence the entire ring and will close the exits if the handler asks (and the judge allows).</li>
                    <li><i class="fas fa-paw"></i>We use rubberized contacts.</li>
                </ul>
